static const uint8_t* font8x16_find(char c)
{
    // digits
    if (c >= '0' && c <= '9') return font8x16_digits[c - '0'];
    // A-Z
    if (c >= 'A' && c <= 'Z') return font8x16_AZ[c - 'A'];
    // a-z -> 映射成 A-Z
    if (c >= 'a' && c <= 'z') return font8x16_AZ[c - 'a'];

    // subset符号
    for (unsigned i = 0; i < sizeof(font8x16_subset)/sizeof(font8x16_subset[0]); i++)
        if (font8x16_subset[i].c == c) return font8x16_subset[i].d;

    return font8x16_subset[0].d; // 空格

}

static const uint8_t* han16_get(uint16_t code)
{
    for (unsigned i = 0; i < sizeof(font_cn_16x16)/sizeof(font_cn_16x16[0]); i++)
        if (font_cn_16x16[i].code == code)
            return font_cn_16x16[i].dot;
    return 0;
}

static const uint8_t* han16_get_chepai(uint16_t code)
{
    for (unsigned i = 0; i < sizeof(font_16x16)/sizeof(font_16x16[0]); i++)
        if (font_16x16[i].code == code)
            return font_16x16[i].dot;
    return 0;
}


static void draw8x16(int x, int y, char c)
{
    const uint8_t *d = font8x16_find(c);
    // 上半 8 行：d[0..7] 每个字节是一列
    for (int col = 0; col < 8; col++)
    {
        uint8_t v = d[col];
        for (int bit = 0; bit < 8; bit++)
            ssd1306_draw_pixel(x + col, y + bit, (v >> bit) & 1);
    }

    // 下半 8 行：d[8..15]
    for (int col = 0; col < 8; col++)
    {
        uint8_t v = d[8 + col];
        for (int bit = 0; bit < 8; bit++)
            ssd1306_draw_pixel(x + col, y + 8 + bit, (v >> bit) & 1);
    }
}

void draw16x16(int x, int y, const uint8_t d[32])
{
    // 上半 8 行
    for (int col = 0; col < 16; col++)
    {
        uint8_t v = d[col];
        for (int bit = 0; bit < 8; bit++)
            ssd1306_draw_pixel(x + col, y + bit, (v >> bit) & 1);
    }

    // 下半 8 行
    for (int col = 0; col < 16; col++)
    {
        uint8_t v = d[16 + col];
        for (int bit = 0; bit < 8; bit++)
            ssd1306_draw_pixel(x + col, y + 8 + bit, (v >> bit) & 1);
    }
}

int oled_draw_cn16(int x, int y, const uint16_t *ucs, int count)
{
    int cx = x;
    for (int i = 0; i < count; i++)
    {
        const uint8_t *dot = han16_get(ucs[i]);
        if (dot) draw16x16(cx, y, dot);
        cx += 16;
    }
    return cx - x;
}

static int utf8_decode_first(const char *s, uint32_t *out_cp)
{
    const uint8_t *p = (const uint8_t*)s;

    if (p[0] < 0x80) {
        *out_cp = p[0];
        return 1;
    }

    // 三字节中文
    if ((p[0] & 0xF0) == 0xE0) {
        *out_cp =
            ((p[0] & 0x0F) << 12) |
            ((p[1] & 0x3F) << 6)  |
            (p[2] & 0x3F);
        return 3;
    }

    return 0;
}

void draw_plate_utf8(int x, int y, const char *plate)
{
    uint32_t cp = 0;
    int n = utf8_decode_first(plate, &cp);

    int cx = x;

    if (n == 3)   // 有中文
    {
        const uint8_t *dot = han16_get_chepai((uint16_t)cp);
        if (dot)
            draw16x16(cx, y, dot);

        cx += 16;
        ssd1306_draw_str(cx, y, plate + n);
    }
    else
    {
        // 没有中文，直接画
        ssd1306_draw_str(cx, y, plate);
    }
}

void ssd1306_draw_pixel(int x, int y, uint8_t color)
{
    if (x < 0 || x >= SSD1306_WIDTH || y < 0 || y >= SSD1306_HEIGHT) return;
    uint32_t idx = x + (y/8) * SSD1306_WIDTH;
    uint8_t mask = 1 << (y % 8);
    if (color) g_buf[idx] |= mask;
    else       g_buf[idx] &= (uint8_t)~mask;
}

void ssd1306_fill(uint8_t color)
{
    memset(g_buf, color ? 0xFF : 0x00, sizeof(g_buf));
}


void ssd1306_draw_str(int x, int y, const char *s)
{
    while (s && *s)
    {
        draw8x16(x, y, *s++);
        x += 8;
        if (x > SSD1306_WIDTH - 8) break;
    }
}

void ssd1306_update(void)
{
    // 设置页地址 0..(HEIGHT/8-1)
    for (uint8_t page = 0; page < (SSD1306_HEIGHT/8); page++)
    {
        wr_cmd(0xB0 | page);
        wr_cmd(0x00);
        wr_cmd(0x10);
        wr_data(&g_buf[SSD1306_WIDTH * page], SSD1306_WIDTH);
    }
}

void ssd1306_init(void)
{
    HAL_Delay(50);

    wr_cmd(0xAE); // display off
    wr_cmd(0x20); wr_cmd(0x00); // horizontal addressing
    wr_cmd(0xB0);
    wr_cmd(0xC8); // COM scan direction remapped
    wr_cmd(0x00);
    wr_cmd(0x10);
    wr_cmd(0x40);
    wr_cmd(0x81); wr_cmd(0x7F); // contrast
    wr_cmd(0xA1); // segment remap
    wr_cmd(0xA6); // normal display
    wr_cmd(0xA8); wr_cmd(SSD1306_HEIGHT - 1);
    wr_cmd(0xA4);
    wr_cmd(0xD3); wr_cmd(0x00);
    wr_cmd(0xD5); wr_cmd(0x80);
    wr_cmd(0xD9); wr_cmd(0xF1);
    wr_cmd(0xDA); wr_cmd(SSD1306_HEIGHT == 64 ? 0x12 : 0x02);
    wr_cmd(0xDB); wr_cmd(0x40);
    wr_cmd(0x8D); wr_cmd(0x14);
    wr_cmd(0xAF); // display on

    ssd1306_fill(0);
    ssd1306_update();
}
