static void wr_cmd(uint8_t cmd)
{
    uint8_t d[2] = {0x00, cmd}; // 0x00=command
    HAL_I2C_Master_Transmit(&hi2c1, SSD1306_I2C_ADDR, d, 2, 100);
}

static void wr_data(const uint8_t *data, uint16_t len)
{
    // SSD1306 I2C：0x40=data
    // 为了简单，分段发，避免一次太长
    uint8_t tmp[17];
    tmp[0] = 0x40;
    while (len)
    {
        uint16_t chunk = (len > 16) ? 16 : len;
        memcpy(&tmp[1], data, chunk);
        HAL_I2C_Master_Transmit(&hi2c1, SSD1306_I2C_ADDR, tmp, chunk + 1, 100);
        data += chunk;
        len  -= chunk;
    }
}

